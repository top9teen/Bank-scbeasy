package com.scbeasy.demo.Bean;

public class ScbeasyPriceBean {

	private int scId;
	private String yeYear;
	private String brName;
	private String scPrice;
	public int getScId() {
		return scId;
	}
	public void setScId(int scId) {
		this.scId = scId;
	}
	public String getYeYear() {
		return yeYear;
	}
	public void setYeYear(String yeYear) {
		this.yeYear = yeYear;
	}
	public String getBrName() {
		return brName;
	}
	public void setBrName(String brName) {
		this.brName = brName;
	}
	public String getScPrice() {
		return scPrice;
	}
	public void setScPrice(String scPrice) {
		this.scPrice = scPrice;
	}
	
	
}
